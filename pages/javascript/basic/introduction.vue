### ネットワーク局でのJavaScript

私たち(ネットワーク局)は、Webページを作ることが最大の目的として存在しています。
そのWebページを作るためにHTMLとCSSを知ったと思いますが、この2つだけだと思った通りの「動き」が作れない時が多々あります。

例えば、ハンバーガーメニューのように何か「ボタン」をクリック(スマホなどの場合タップ)することで「メニュー」が出てくるようなもの。
例えば、少しページを下にスクロールすると文字や画像が綺麗な装飾とともに出現するような仕組み。
例えば、Webページを開いたときに1/10程度の確率で何か別の画像が出現したり、ないしは別のページに飛ばされたりするような仕組み。

こういった物は、HTMLとCSSのみでは作ることは不可能でしょう。
(本気を出せば作れるようなものもありますが、HTML,CSSの本来の目的からは外れてしまうことが多いです。)
そのために私たちはJavaScriptを使います。これを使うことで、例として挙げた上の3つは実装することができます。
(本旨からは逸れますが、JavaScriptを使ってブラウザゲームを作ることもできます)

