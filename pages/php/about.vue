それに対し、PHPはサーバ側で動き、その結果をブラウザに送信する用のプログラミング言語です。

ちなみにブラウザで動くものはユーザから見て手前にあるのでフロントエンド、サーバで動くものは奥なのでバックエンドと呼びます。
PHPはバックエンドで動く用に作られたプログラミング言語です。


